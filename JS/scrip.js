        const gallery = document.getElementById('gallery');
        const searchInput = document.getElementById('search');
        const gridViewBtn = document.getElementById('grid-view');
        const listViewBtn = document.getElementById('list-view');
        const loadMoreBtn = document.getElementById('load-more');
        const modal = document.getElementById('modal');
        const modalImage = document.getElementById('modal-image');
        const modalCaption = document.getElementById('modal-caption');
        const closeModalBtn = document.getElementById('close-modal');
        const prevBtn = document.getElementById('prev-btn');
        const nextBtn = document.getElementById('next-btn');

        
        let currentView = 'grid';
        let visiblePhotos = 12; 
        let currentPhotoIndex = 0;
        let filteredPhotos = [...photosConfig.photos];

        
        function renderPhotos() {
            gallery.innerHTML = '';
            
            const photosToShow = filteredPhotos.slice(0, visiblePhotos);
            
            photosToShow.forEach((photo, index) => {
                const photoCard = document.createElement('div');
                photoCard.className = 'photo-card';
                photoCard.dataset.index = index;
                
                photoCard.innerHTML = `
                    <div class="photo-img">
                        <img src="${photosConfig.baseFolder}${photo.filename}" alt="${photo.title}" onerror="this.src='/api/placeholder/400/300';this.onerror='';">
                    </div>
                    <div class="photo-info">
                        <h3 class="photo-title">${photo.title}</h3>
                        <div class="photo-date">${photo.date}</div>
                    </div>
                `;
                
                photoCard.addEventListener('click', () => openModal(index));
                gallery.appendChild(photoCard);
            });
            
           
            loadMoreBtn.style.display = visiblePhotos >= filteredPhotos.length ? 'none' : 'block';
        }

    
        function changeView(view) {
            currentView = view;
            if (view === 'grid') {
                gallery.classList.remove('list-view');
                gridViewBtn.classList.add('active');
                listViewBtn.classList.remove('active');
            } else {
                gallery.classList.add('list-view');
                listViewBtn.classList.add('active');
                gridViewBtn.classList.remove('active');
            }
        }

       
        function openModal(index) {
            const photo = filteredPhotos[index];
            currentPhotoIndex = index;
            
            modalImage.src = `${photosConfig.baseFolder}${photo.filename}`;
            modalImage.onerror = function() {
                this.src = '/api/placeholder/800/600';
                this.onerror = null;
            };
            
            modalCaption.textContent = `${photo.title} - ${photo.date}`;
            modal.classList.add('active');
            
            
            updateModalNavButtons();
            
          
            document.body.style.overflow = 'hidden';
        }

     
        function closeModal() {
            modal.classList.remove('active');
            document.body.style.overflow = '';
        }

     
        function nextPhoto() {
            if (currentPhotoIndex < filteredPhotos.length - 1) {
                currentPhotoIndex++;
                updateModal();
            }
        }

       
        function prevPhoto() {
            if (currentPhotoIndex > 0) {
                currentPhotoIndex--;
                updateModal();
            }
        }

     
        function updateModal() {
            const photo = filteredPhotos[currentPhotoIndex];
            modalImage.src = `${photosConfig.baseFolder}${photo.filename}`;
            modalCaption.textContent = `${photo.title} - ${photo.date}`;
            updateModalNavButtons();
        }

        
        function updateModalNavButtons() {
            prevBtn.style.visibility = currentPhotoIndex > 0 ? 'visible' : 'hidden';
            nextBtn.style.visibility = currentPhotoIndex < filteredPhotos.length - 1 ? 'visible' : 'hidden';
        }

       
        function filterPhotos() {
            const searchTerm = searchInput.value.toLowerCase();
            filteredPhotos = photosConfig.photos.filter(photo => 
                photo.title.toLowerCase().includes(searchTerm) || 
                photo.date.toLowerCase().includes(searchTerm)
            );
            
            visiblePhotos = 12; 
            renderPhotos();
        }

        
        function loadMorePhotos() {
            visiblePhotos += 12;
            renderPhotos();
        }

       
        gridViewBtn.addEventListener('click', () => changeView('grid'));
        listViewBtn.addEventListener('click', () => changeView('list'));
        loadMoreBtn.addEventListener('click', loadMorePhotos);
        closeModalBtn.addEventListener('click', closeModal);
        prevBtn.addEventListener('click', prevPhoto);
        nextBtn.addEventListener('click', nextPhoto);
        searchInput.addEventListener('input', filterPhotos);

        
        document.addEventListener('keydown', (e) => {
            if (e.key === 'Escape') closeModal();
            if (e.key === 'ArrowRight' && modal.classList.contains('active')) nextPhoto();
            if (e.key === 'ArrowLeft' && modal.classList.contains('active')) prevPhoto();
        });

      
        renderPhotos();